<h3>Signing of the contract</h3>
<br>
<p>The fitout contractor should be willing to sign a contract giving details of the deliverables from each side with timelines and payment schedule for completion. They should have a single point of contact to coordinate the work schedule and various issues that may arise during the implementation phase.
